    std::vector<int> segment_values;
    std::string display_previous_command;


    static void build_frame()
    // Sets the canvas for drawing.
    {
        SetConsoleTitleA(":..dCount..:");
        HWND console = GetConsoleWindow();
        RECT ConsoleRect;
        GetWindowRect(console, &ConsoleRect);
        MoveWindow(console, ConsoleRect.left, ConsoleRect.top, 1125, 750, TRUE);
        HANDLE hOut = GetStdHandle(STD_OUTPUT_HANDLE);
        CONSOLE_SCREEN_BUFFER_INFO scrBufferInfo;
        GetConsoleScreenBufferInfo(hOut, &scrBufferInfo);
        COORD newSize;
        newSize.X = 136; // scrBufferWidth;
        newSize.Y = 44; //winHeight;
        SetConsoleScreenBufferSize(hOut, newSize);
    }


    static void clear_screen()
    // Clears the canvas for rendering.
    // Should be applied before new screen draws.
    {
        COORD topLeft = { 0, 0 };
        HANDLE console = GetStdHandle(STD_OUTPUT_HANDLE);
        CONSOLE_SCREEN_BUFFER_INFO screen;
        DWORD written;

        GetConsoleScreenBufferInfo(console, &screen);
        FillConsoleOutputCharacterA(
            console, ' ', screen.dwSize.X *screen.dwSize.Y, topLeft, &written
        );
        FillConsoleOutputAttribute(
            console, FOREGROUND_GREEN | FOREGROUND_RED | FOREGROUND_BLUE,
            screen.dwSize.X *screen.dwSize.Y, topLeft, &written
        );
        SetConsoleCursorPosition(console, topLeft);
    }


    void draw_screen()
    {
        std::cout << display_top;
        for(int i = 0; i < 16; ++i)
        {
            std::cout << display_column_left   << arabic_numerals[segment_values[0]][i];
            std::cout << display_column_middle << arabic_numerals[segment_values[1]][i];
            std::cout << display_column_middle << arabic_numerals[segment_values[2]][i];
            std::cout << display_column_middle << arabic_numerals[segment_values[3]][i];
            std::cout << display_column_right;
        }
        std::cout << display_bottom;
        std::cout << ui_top << display_previous_command << '\n';
        std::cout << ui_bottom;
    }


